package com.huto.forces_of_reality.render.effects;

public class LectorVectorColorData {

	float red, green, blue, alpha;

	public LectorVectorColorData(float redIn, float greenIn, float blueIn, float alphaIn) {
		this.red = redIn;
		this.green = greenIn;
		this.blue = blueIn;
		this.alpha = alphaIn;
	}

	public float getRed() {
		return red;
	}

	public void setRed(float red) {
		this.red = red;
	}

	public float getGreen() {
		return green;
	}

	public void setGreen(float green) {
		this.green = green;
	}

	public float getBlue() {
		return blue;
	}

	public void setBlue(float blue) {
		this.blue = blue;
	}

	public float getAlpha() {
		return alpha;
	}

	public void setAlpha(float alpha) {
		this.alpha = alpha;
	}

}
